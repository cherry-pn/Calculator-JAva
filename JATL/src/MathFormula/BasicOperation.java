package MathFormula;

public class BasicOperation {
    public double add(double sided, double bladed){
         return sided+bladed;
    }
    public double sub(double sided, double bladed){
         return sided-bladed;
    }
    public double multi(double sided, double bladed){
         return sided*bladed;
    }
    public double div(double sided, double bladed){
         return sided/bladed;
    }
    
}
